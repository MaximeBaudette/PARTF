function [MagErrRmp,AngErrRmp,FreqErrRmp,ROCOFErrRmp] = C37PmuFreqRamp(...
    Rf,...
    Xm,...
    theta0,...
    F0,...
    Fin,...
    PhaseID,...
    t,...
    MagFreqRes)

a1M = 0.03163*Xm/70*sin(2*pi/11.6*(Fin-56.7)) ;
ferr0 = 2*(F0-Fin);
thetaM = theta0; % or -2*tehta0??
a2M = Xm*(MagFreqRes-1);
MagErrRmp = a1M*cos(2*pi*ferr0*t-pi*2*Rf*t.^2+thetaM)+a2M;

a1P = 0.02589*sin(2*pi/11.6*(Fin-56.7));
thetaP = theta0-pi/2; % or -2*theta0-pi/2?
AngErrRmp = a1P*cos(2*pi*ferr0*t-pi*2*Rf*t.^2+thetaP);

FreqErrRmp = 0;

ROCOFErrRmp = 0;
