function [MagErrRag,AngErrRag,FreqErrRag,ROCOFErrRag] = C37PmuRag(...
    Xm,...
    Fin,...
    F0,...
    theta0,...
    MagFreqRes,...
    t...
    )
a1M = 0.03163*Xm/70*sin(2*pi/11.6*(Fin-56.7)) ;
ferr = 2*(F0 - Fin);
a2M = Xm*(MagFreqRes-1);
thetaM = -2*theta0;
MagErrRag = a1M*cos(2*pi*ferr*t+thetaM)+a2M;

a1P = 0.02589*sin(2*pi/11.6*(Fin-56.7));
a2P = 0;
thetaP = -2*theta0-pi/2;
AngErrRag = a1P*cos(2*pi*ferr*t+thetaP)+a2P;    % degree

FreqErrRag = 0;

ROCOFErrRag = 0;